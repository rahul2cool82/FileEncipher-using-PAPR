    private static String bytesToString(byte[] encrypted)
    {
        String test = "";
        for (byte b : encrypted)
        {
            test += Byte.toString(b);
        }
        return test;
    }
    
    
    public static byte[] encrypt(byte[] message,BigInteger pk, BigInteger n)
    {
        //System.out.println("pk : " +pk);
        //System.out.println("N  : "+n);
        return (new BigInteger(message)).modPow(pk,n).toByteArray();
        
    }
    
