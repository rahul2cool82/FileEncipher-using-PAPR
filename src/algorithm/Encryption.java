    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here:
        try{
        MessageDigest md = MessageDigest.getInstance("MD5");
        FileInputStream fis = new FileInputStream(jTextField1.getText());

        byte[] dataBytes = new byte[1024];

        int nread = 0;
        while ((nread = fis.read(dataBytes)) != -1) {
            md.update(dataBytes, 0, nread);
        };
        byte[] mdbytes = md.digest();
        StringBuffer sb = new StringBuffer();
        for (int i = 0; i < mdbytes.length; i++) {
            sb.append(Integer.toString((mdbytes[i] & 0xff) + 0x100, 16).substring(1));
        }
        //System.out.println("Digest(in hex format):: " + sb.toString());
        jTextField3.setText(sb.toString());
        }catch(Exception e){
            
        }
        
        /* Now Digital Signature*/
        
        
        try{
                String qry1="select prkey, N from allkeys where userid ='"+userid+"'";
                //System.out.println(qry1);
                ResultSet rs;
                rs=st.executeQuery(qry1);
                rs.next();
                String pr=rs.getString(1);
                String N=rs.getString(2);
                //System.out.println(pr);
                //System.out.println(N);
                uprk=new BigInteger(pr);
                uN=new BigInteger(N);
                RSAPrivateKeySpec privateSpec = new RSAPrivateKeySpec(uN, uprk);
                KeyFactory kf = KeyFactory.getInstance("RSA");
                Key privateKey = kf.generatePrivate(privateSpec);
                byte[] data = jTextField3.getText().getBytes("UTF8");

                Signature sig = Signature.getInstance("MD5WithRSA");
                
                
                sig.initSign(kf.generatePrivate(privateSpec));
                sig.update(data);
                byte[] signatureBytes = sig.sign();
                //System.out.println("Singature:" + new BASE64Encoder().encode(signatureBytes));
                DigitalSign=new BASE64Encoder().encode(signatureBytes);
                DigitalSign=DatatypeConverter.printBase64Binary(DigitalSign.getBytes());
        }catch(Exception e){
            
        }


    }//GEN-LAST:event_jButton3ActionPerformed

